class Solution {
public:
    ListNode* deleteDuplicates(ListNode* h){
        if(!h){
            return h;
        }

        ListNode* c=h;

        while(c&&c->next){
            if(c->val==c->next->val){
                c->next=c->next->next;
            } else{
                c=c->next;
            }
        }

        return h;
    }
};